class Product(BaseModel):
    big_category = models.ForeignKey(Bigcategory, on_delete=models.SET_NULL, null=True ,related_name='big_product')
    middle_category = models.ForeignKey(MiddleCategory, on_delete=models.SET_NULL, null=True, related_name='middle_product')
    small_category = models.ForeignKey(SmalllCategory, on_delete=models.SET_NULL, null=True, related_name='small_product')
    name = models.CharField(max_length=255, verbose_name="Nomi")
